// Language management functions
function getCurrentLanguage() {
    return localStorage.getItem('selectedLanguage') || 'en';
}

function setLanguage(lang) {
    localStorage.setItem('selectedLanguage', lang);
    translatePage();
}

function translatePage() {
    const lang = getCurrentLanguage();
    const elements = document.querySelectorAll('[data-translate]');

    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' && element.type === 'text') {
                element.placeholder = translations[lang][key];
            } else if (element.tagName === 'INPUT' && element.type === 'password') {
                element.placeholder = translations[lang][key];
            } else if (element.tagName === 'INPUT' && element.type === 'number') {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Update page title
    const titleElement = document.querySelector('title');
    if (titleElement) {
        const page = window.location.pathname.split('/').pop().split('.')[0];
        if (page === 'index' || page === '') {
            titleElement.textContent = `Welcome | Rural Banking - ${translations[lang].home}`;
        } else if (page === 'login') {
            titleElement.textContent = `${translations[lang].login} | Rural Banking`;
        } else if (page === 'dashboard') {
            titleElement.textContent = `${translations[lang].dashboard} | Rural Banking`;
        } else if (page === 'transaction') {
            titleElement.textContent = `${translations[lang].transaction} | Rural Banking`;
        } else if (page === 'receipt') {
            titleElement.textContent = `${translations[lang].receipt} | Rural Banking`;
        }
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function () {
    // Set the language selector to current language
    const langSelect = document.getElementById('languageSelect');
    if (langSelect) {
        langSelect.value = getCurrentLanguage();
    }
    translatePage();
});
